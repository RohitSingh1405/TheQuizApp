   class SplashThread extends Thread
   {
       public void run()
       {
           int count=1;
           Random r=new Random();
           while(Progress.getValue()<Progress.getMaximum())
           {
               try
               {
                   Progress.setValue(count);
                   Thread.sleep(r.nextInt(2000));
                   count+=(r.nextInt(100)%20);
                   
               }
               catch(InterruptedException ex)
               {
                   JOptionPane.showMessageDialog(null,"EXCEPTION IN THREAD "+ex,"ERROR",JOptionPane.ERROR_MESSAGE);
               }
           }
       LoginFrame newLoginFrame=new LoginFrame();
       dispose();
       newLoginFrame.setVisible(true);
       }
   }

