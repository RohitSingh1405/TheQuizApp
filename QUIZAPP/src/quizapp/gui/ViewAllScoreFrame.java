private void showDataInTable()
{
   try
        {
        
        ArrayList<Performance> performanceList=PerformanceDAO.getAllData();
        if(performanceList.isEmpty()==true)
          JOptionPane.showMessageDialog(null, "Sorry! No student has yet given any exam","Error!",JOptionPane.INFORMATION_MESSAGE);  



        else
        {
                        DefaultTableModel model=(DefaultTableModel)jtStudentData.getModel();
		Object[] rows=new Object[7];

            for(Performance obj: performanceList)
            {
                rows[0]=obj.getUserId();
                rows[1]=obj.getExamId();
                rows[6]=obj.getLanguage();
                rows[2]=obj.getRight();
                rows[3]=obj.getWrong();
                rows[4]=obj.getUnAttempted();
                rows[5]=obj.getPercentage();
                model.addRow(rows);
            }

}
        }
   catch(SQLException e)
   {
       JOptionPane.showMessageDialog(null,"SQL ERROR"+e,"EXCEPTION",JOptionPane.INFORMATION_MESSAGE);
       e.printStackTrace();
   }
}
   
